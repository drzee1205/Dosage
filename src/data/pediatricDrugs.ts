// Helper function to get all drugs from all categories
export function getAllDrugs() {
  const allDrugs = []
  for (const category in pediatricDrugs) {
    allDrugs.push(...pediatricDrugs[category as keyof typeof pediatricDrugs])
  }
  return allDrugs
}

// Helper function to get drugs by category
export function getDrugsByCategory(category: string) {
  return pediatricDrugs[category as keyof typeof pediatricDrugs] || []
}

// Helper function to search drugs
export function searchDrugs(query: string) {
  const allDrugs = getAllDrugs()
  const lowercaseQuery = query.toLowerCase()
  
  return allDrugs.filter(drug => 
    drug.name.toLowerCase().includes(lowercaseQuery) ||
    drug.genericName?.toLowerCase().includes(lowercaseQuery) ||
    drug.brandNames?.toLowerCase().includes(lowercaseQuery) ||
    drug.indications.toLowerCase().includes(lowercaseQuery)
  )
}